public class bai13_for {
    public static void main(String[] args) {
        int tong = 0;
        for (int i = 1; i<12; i+=2)
            tong+=i;
        System.out.println("tong =" +tong);

        int hieu = 0;
        for (int x = 19; x>10; x--){
            hieu-=x;}
        System.out.println(+hieu);
    }

}
